                     <h3>Sobre o PIX</h3>
                     <br>
                     <h4>Historia do PIX</h4>
                     <h5>Fonte: https://pt.wikipedia.org/wiki/Pix</h5>
                     <p>Pix é um meio de pagamento eletrônico do Brasil lançado oficialmente no dia 5 de outubro de 2020, com início de funcionamento integral em 16 de novembro de 2020. Suas chaves de transação (conhecidas como chaves Pix) podem ser cadastradas utilizando os números do telefone, CPF ou endereço de e-mail do usuário, ou mesmo através de uma chave aleatória que possibilita o acesso aos dados bancários do usuário da conta e realizar a transação imediatamente. O Pix faz parte do Sistema de Pagamentos Instantâneos (SPI), que foi estabelecido pelo comunicado nº 32.927, de 21 de dezembro de de 2018 do Banco Central do Brasil (BACEN) e depois atualizado pelo comunicado n° 34.085 de 28 de agosto de 2019.</p>
                     <br>
                     <p>O nome escolhido pelo Banco Central, na verdade, não é nenhuma sigla, mas um termo que remete a conceitos como tecnologia, transação e pixel. A ideia é ser tão simples como um bate-papo em redes sociais, inclusive no nome. Ao contrário do que muitos haviam difundido anteriormente, o Pix não é uma criptomoeda, mas sim um meio de pagamento instantâneo. As transações são feitas em real brasileiro (R$).</p>
                     <br>
                     <p>O presidente do BC, Roberto Campos Neto, afirmou que este é um dos projetos mais importantes da instituição para aquele ano e destacou que o Pix vai baratear o custo das transferências e vai eliminar a necessidade de as pessoas portarem dinheiro físico, o que representa um custo, principalmente para as empresas.</p>
