                     <h4>UniAteneu</h4>
                     <p>O Centro Universitário Ateneu tem como finalidade educar e formar homens e mulheres em autênticos cidadãos conscientes de suas responsabilidades, comprometidos com os valores éticos, sociais, humanos, culturais e profissionais, contribuindo através do ensino, da pesquisa e divulgação científica, para o desenvolvimento sustentável da sociedade. Assim, promovemos o desenvolvimento integral dos nossos alunos, preparando-os como cidadãos e profissionais capazes de se integrarem ao mundo do trabalho e intervirem na sociedade de forma equilibrada com uma formação humanística, crítica e reflexiva. Nossos cursos se diferenciam pela qualidade e a variedade em diversas áreas do conhecimento, distribuídos em cursos de pós-graduação, graduação, tecnólogos e técnicos, além de cursos de extensão e de preparação para concursos. Partindo deste princípio, entendemos que nossos alunos terão capacidade de colocar em prática a ciência e a técnica sempre a serviço da sociedade, colaborando através dos conhecimentos adquiridos na UniAteneu para a construção de um mundo melhor, baseando-se no respeito aos valores humanos e na equidade. <br><br>Para mais informações acesse o site da instituição em <a href="https://uniateneu.edu.br/" target="_block">www.uniateneu.edu.br</a>!</p>
                     <br>
                     <h4>Sobre o Projeto</h4>
                     <p>Nós, alunos do curso de Análise e Desenvolvimento de Sistemas do Centro Universitário UNIATENEU, buscando descomplicar o uso de tecnologias bancárias para pessoas com dificuldades. Resolvemos colocar online nosso blog, trazendo novidades, dicas de segurança, tudo e mais um pouco relacionado ao PIX. 
                     Através daqui, vamos atualizar vocês sobre quaisquer atualização emitida pelo Banco Central, e principalmente buscar alertá-los sobre as fraudes mais aplicadas no mercado.</p>
